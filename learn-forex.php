                    <h3 class="mt-6 mb-1 mb-lg-2">What is a forex pair?</h3>
                    <p class="fs-six ">A pair of currencies that are traded against one another is known as a forex
                        pair. Though there are countless possible pairings, some of the most well-liked ones include the
                        US dollar versus the Japanese yen (USD/JPY), the euro against the US dollar (EUR/USD), and the
                        British pound against the US dollar (GBP/USD).</p>
                    <p class="fs-six">In a currency pair, the price is always on the right and the base currency
                        is always on the left. The quoted currency is equal to the current quotation price of the pair,
                        which indicates how much of the quote currency will be needed to purchase one base. The base
                        currency is always equal to one. So, when you’re exchanging currency, you’re constantly selling
                        one to get another.</p>
                    <h3 class="mt-6 mb-1 mb-lg-3">What are the base and quote currencies?</h3>
                    <p class="fs-six ">The first listed currency in a currency pair in forex trading is called the
                        base currency, and the second listed currency is called the quote currency. The quantity of the
                        quote currency that is equal to one unit of the base currency is represented by its value, which
                        is always equal to one unit of the base currency. For instance, the base currency in the EUR/USD
                        currency pair is EUR, while the quote currency is USD.</p>
                    <h3 class="mt-6 mb-1 mb-lg-3">What is a pip in forex?</h3>
                    <h5 class="fs-six">Definition :</h5>
                    <p class="fs-six ">In forex trading, a pip is the smallest unit of price change. It stands for
                        "percentage in point" or "price interest point." It shows how a currency pair's exchange rate
                        has changed.</p>
                    <h5 class="fs-six  mb-2 mt-2">Measurement :</h5>
                    <p class="fs-six ">Four decimal places are supplied for the majority of currency pairings. A
                        one pip movement would occur, for instance, if the EUR/USD currency pair moved from 1.1234 to
                        1.1235.</p>
                    <h5 class="fs-six  mb-2 mt-2">Value :</h5>
                    <p class="fs-six ">The amount of the deal (lot size) and the currency pair being traded
                        determine a pip's value. Generally speaking, one pip corresponds to ten units of the quote
                        currency for a regular lot of 100,000 units of the base currency.</p>
                    <h5 class="fs-six  mb-2 mt-2">Significance :</h5>
                    <p class="fs-six ">In forex trading, pip values are essential to calculating gains and losses.
                        They help traders by precisely measuring price changes and calculating the possible profits or
                        losses on a deal.</p>
                    <h3 class="mt-6  mb-lg-3">What is a lot in forex trading?</h3>
                    <p class="fs-six">In forex trading, a lot is defined as a specified quantity of currency
                        units. It shows the magnitude of a deal that an individual trader is making.</p>
                    <h5 class="fs-six  mb-2 mt-2">Different Lot Types : In forex trading, lots come in several
                        forms :</h5>
                    <p class="fs-six ">100,000 units of the base currency make up a standard lot.</p>
                    <p class="fs-six ">10,000 units of the basic currency make up a mini lot.</p>
                    <p class="fs-six ">1,000 units of the base currency make up a micro lot.</p>
                    <h5 class="fs-six  mb-2 mt-2">Risk management :</h5>
                    <p class="fs-six ">A key component of risk management is lot size. Smaller lot sizes lower the
                        risk exposure each transaction, but larger lot sizes increase the possible gains or losses per
                        pip movement.</p>
                    <h5 class="fs-six  mb-2 mt-2">Flexibility :</h5>
                    <p class="fs-six ">Forex firms frequently provide traders with the option to select the lot
                        size that best suits their trading style and risk tolerance.</p>
                    <h5 class="fs-six mb-2 mt-2">Leverage :</h5>
                    <p class="fs-six ">In forex trading, lot sizes and leverage are directly related. Although more
                        leverage lowers the required capital for traders to hold greater positions, it also raises the
                        potential danger. To safeguard their trading accounts, traders must so carefully control lot
                        sizes and leverage.</p>
