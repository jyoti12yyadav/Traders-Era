                        strengthens a currency, while negative data may weaken it. Traders closely monitor these reports
                        and react swiftly to news that could affect currency values.</p>
                    <h5 class="fs-six mb-2 mt-2">Market Sentiment :</h5>
                    <p class="fs-six ">Market sentiment refers to the overall attitude of traders towards a
